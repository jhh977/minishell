#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	i;
	size_t	j;

	i = 0;
	while (src[i] != '\0')
	{
		i++;
	}
	if (size == 0)
	{
		return (i);
	}
	j = 0;
	while (*src && j < size -1)
	{
		*dest = *src;
		src++;
		dest++;
		j++;
	}
	*dest = '\0';
	return (i);
}
