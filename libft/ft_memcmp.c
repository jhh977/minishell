#include "libft.h"

int	ft_memcmp(const void *dest, const void *src, size_t n)
{
	size_t				i;
	const unsigned char	*d;
	const unsigned char	*s;

	d = (const unsigned char *)dest;
	s = (const unsigned char *)src;
	i = 0;
	while (i < n)
	{
		if (d[i] != s[i])
			return (d[i] - s[i]);
		i++;
	}
	return (0);
}
