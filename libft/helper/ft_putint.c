#include "../libft.h"

int	ft_putint(int n)
{
	int	i;

	i = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
		i++;
	}
	if (n >= 10)
	{
		i += ft_putint(n / 10);
	}
	i += ft_putchar(n % 10 + '0');
	return (i);
}
