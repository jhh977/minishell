#include "../libft.h"

int	ft_putstr(char *s)
{
	size_t	i;

	if (!s)
		return (ft_putstr("(null)"));
	i = 0;
	while (s[i] != '\0')
		i += ft_putchar(s[i]);
	return (i);
}
